#23. Faça um algoritmo que leia os valores A, B, C e imprima na tela se a soma de A + B é menor que C.
a = int(input("Insira o valor A: "))
b = int(input("Insira o valor B: "))
c = int(input("Insira o valor C: "))
if ((a + b) < c):
    print("A soma é menor que C")
else:
    print("A soma não é menor que C")





